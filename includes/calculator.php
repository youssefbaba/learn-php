<?php

function addition($number1 = 0 , $number2 = 0)
{
     return $number1 + $number2 ;
} 


function substration($number1 = 0 , $number2 = 0)
{
     return $number1 -  $number2 ;
} 


function mulitiplication($number1 = 0  , $number2 = 0)
{
     return $number1 * $number2 ;
} 


function division ($number1 = 0 , $number2 = 0)
{
    if ($number2 != 0) {
        return $number1 / $number2 ;
    } else {
        return null ;
    }
    

} 












